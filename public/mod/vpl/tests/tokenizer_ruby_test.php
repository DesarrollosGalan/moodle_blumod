namespace mod_vpl;

use mod_vpl\util\assertf;
use mod_vpl\tokenizer\token_type;
use mod_vpl\tokenizer\tokenizer_factory;

/**
 * Unit tests for the Ruby tokenizer.
 *
 * @group mod_vpl
 * @group mod_vpl_vplt
 * @group mod_vpl_tokenizer
 * @group mod_vpl_tokenizer_lang
 * @covers \mod_vpl\tokenizer\tokenizer
 * @covers \mod_vpl\tokenizer\tokenizer_factory
 */
final class tokenizer_ruby_test extends \advanced_testcase {
    /**
     * Test that the Ruby tokenizer can parse a code example.
     *
     * @covers \mod_vpl\tokenizer\tokenizer_factory::get
     * @covers \mod_vpl\tokenizer\tokenizer::parse
     */
    public function test_tokenizer_parse(): void {
        $this->resetAfterTest();

        $code = '# Ruby example
PI = 3.14159

def factorial(n)
  return 1 if n <= 1
  n * factorial(n - 1)
end

def greet(name)
  puts "Hello, #{name}!"
end

class Animal
  attr_reader :name

  def initialize(name)
    @name = name
  end

  def speak
    "#{@name} makes a sound."
  end
end

# Main
x = 42
message = "Hello, World!"
puts message
greet("World")

=begin
Multi-line comment
=end

numbers = [1, 2, 3, 4, 5]
squares = numbers.map { |n| n ** 2 }
puts squares.inspect

numbers.each do |n|
  puts n
end

if x > 0
  puts "Positive"
elsif x < 0
  puts "Negative"
else
  puts "Zero"
end

puts "5! = #{factorial(5)}"
';

        $tokenizer = tokenizer_factory::get('ruby');
        $this->assertNotNull($tokenizer, 'Failed to create Ruby tokenizer');

        $tokens = $tokenizer->parse($code, false);
        $this->assertIsArray($tokens, 'Tokens should be an array');
        $this->assertNotEmpty($tokens, 'No tokens generated for Ruby');

        $tokentypes = array_unique(array_map(fn($t) => $t->type, $tokens));
        $expectedtypes = [token_type::RESERVED, token_type::IDENTIFIER, token_type::LITERAL, token_type::OPERATOR];
        $found = false;
        foreach ($expectedtypes as $expected) {
            if (in_array($expected, $tokentypes)) {
                $found = true;
                break;
            }
        }
        $this->assertTrue($found, 'Ruby tokenizer should produce at least one expected token type');
    }

    /**
     * Prepare test cases before the execution.
     */
    public static function setUpBeforeClass(): void {
        parent::setUpBeforeClass();
        assertf::set_enable();
    }

    /**
     * Clean up after the execution of test cases.
     */
    public static function tearDownAfterClass(): void {
        assertf::set_disable();
        parent::tearDownAfterClass();
    }
}
