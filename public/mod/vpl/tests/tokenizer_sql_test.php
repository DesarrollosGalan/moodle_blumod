namespace mod_vpl;

use mod_vpl\util\assertf;
use mod_vpl\tokenizer\token_type;
use mod_vpl\tokenizer\tokenizer_factory;

/**
 * Unit tests for the SQL tokenizer.
 *
 * @group mod_vpl
 * @group mod_vpl_vplt
 * @group mod_vpl_tokenizer
 * @group mod_vpl_tokenizer_lang
 * @covers \mod_vpl\tokenizer\tokenizer
 * @covers \mod_vpl\tokenizer\tokenizer_factory
 */
final class tokenizer_sql_test extends \advanced_testcase {
    /**
     * Test that the SQL tokenizer can parse a code example.
     *
     * @covers \mod_vpl\tokenizer\tokenizer_factory::get
     * @covers \mod_vpl\tokenizer\tokenizer::parse
     */
    public function test_tokenizer_parse(): void {
        $this->resetAfterTest();

        $code = '-- SQL example

CREATE TABLE employees (
    id       INTEGER PRIMARY KEY,
    name     VARCHAR(100) NOT NULL,
    salary   DECIMAL(10, 2),
    dept_id  INTEGER REFERENCES departments(id)
);

/* Multi-line comment */
INSERT INTO employees (id, name, salary, dept_id)
VALUES (1, "Alice", 75000.00, 10),
       (2, "Bob",   82000.50, 20);

SELECT e.id, e.name, e.salary, d.name AS department
FROM   employees e
JOIN   departments d ON e.dept_id = d.id
WHERE  e.salary > 70000
ORDER  BY e.salary DESC;

UPDATE employees
SET    salary = salary * 1.10
WHERE  dept_id = 10;

DELETE FROM employees
WHERE  id = 2;

CREATE INDEX idx_employees_dept ON employees(dept_id);
';

        $tokenizer = tokenizer_factory::get('sql');
        $this->assertNotNull($tokenizer, 'Failed to create SQL tokenizer');

        $tokens = $tokenizer->parse($code, false);
        $this->assertIsArray($tokens, 'Tokens should be an array');
        $this->assertNotEmpty($tokens, 'No tokens generated for SQL');

        $tokentypes = array_unique(array_map(fn($t) => $t->type, $tokens));
        $expectedtypes = [token_type::RESERVED, token_type::IDENTIFIER, token_type::LITERAL, token_type::OPERATOR];
        $found = false;
        foreach ($expectedtypes as $expected) {
            if (in_array($expected, $tokentypes)) {
                $found = true;
                break;
            }
        }
        $this->assertTrue($found, 'SQL tokenizer should produce at least one expected token type');
    }

    /**
     * Prepare test cases before the execution.
     */
    public static function setUpBeforeClass(): void {
        parent::setUpBeforeClass();
        assertf::set_enable();
    }

    /**
     * Clean up after the execution of test cases.
     */
    public static function tearDownAfterClass(): void {
        assertf::set_disable();
        parent::tearDownAfterClass();
    }
}
