namespace mod_vpl;

use mod_vpl\util\assertf;
use mod_vpl\tokenizer\tokenizer_factory;

/**
 * Unit tests for the plain text tokenizer.
 *
 * @group mod_vpl
 * @group mod_vpl_vplt
 * @group mod_vpl_tokenizer
 * @group mod_vpl_tokenizer_lang
 * @covers \mod_vpl\tokenizer\tokenizer
 * @covers \mod_vpl\tokenizer\tokenizer_factory
 */
final class tokenizer_text_test extends \advanced_testcase {
    /**
     * Test that the plain text tokenizer can be loaded and returns an array.
     * The text tokenizer produces no tokens by design (it is used as a base
     * for other tokenizers rather than as a standalone language tokenizer).
     *
     * @covers \mod_vpl\tokenizer\tokenizer_factory::get
     * @covers \mod_vpl\tokenizer\tokenizer::parse
     */
    public function test_tokenizer_parse(): void {
        $this->resetAfterTest();

        $code = 'This is a plain text example.
It contains words and numbers like 42 and 3.14.
Special characters: !@#$%^&*()
"Quoted text" and \'single-quoted\' text.
Multiple lines of content.';

        $tokenizer = tokenizer_factory::get('text');
        $this->assertNotNull($tokenizer, 'Failed to create plain text tokenizer');

        $tokens = $tokenizer->parse($code, false);
        // The text tokenizer is intentionally minimal; it may return an empty array.
        $this->assertIsArray($tokens, 'Tokens should be an array');
    }

    /**
     * Prepare test cases before the execution.
     */
    public static function setUpBeforeClass(): void {
        parent::setUpBeforeClass();
        assertf::set_enable();
    }

    /**
     * Clean up after the execution of test cases.
     */
    public static function tearDownAfterClass(): void {
        assertf::set_disable();
        parent::tearDownAfterClass();
    }
}
